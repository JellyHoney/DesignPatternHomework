class SetMeal {
    private String mainMeal;
    private String juice;
    public String getMainMeal(){
        return mainMeal;
    }
    public String getJuice(){
        return juice;
    }
    public void setMainMeal(String mainMeal){
        this.mainMeal=mainMeal;
    }
    public void setJuice(String juice){
        this.juice=juice;
    }
}
